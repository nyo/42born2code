char	*ft_strrev(char *str)
{
	int i;
	int j;
	int temporary;

	i = 0;
	j = 0;
	while (str[i])
	{
		i += 1;
	}
	i -= 1;
	while (j < i)
	{
		temporary = str[i];
		str[i] = str[j];
		str[j] = temporary;
		i -= 1;
		j += 1;
	}
	return (str);
}
