char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i])
		i += 1;
	while (j < nb && src[j])
	{
		dest[i + j] = src[j];
		j += 1;
	}
	dest[i + j] = '\0';
	return (dest);
}
