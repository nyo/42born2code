int		ft_putchar(char c);

void	ft_print_comb(void)
{
	int nb[3];

	nb[0] = 0;
	while (nb[0] < 10)
	{
		nb[1] = nb[0] + 1;
		while (nb[1] < 10)
		{
			nb[2] = nb[1] + 1;
			while (nb[2] < 10)
			{
				ft_putchar('0' + nb[0]);
				ft_putchar('0' + nb[1]);
				ft_putchar('0' + nb[2]);
				if (nb[0] != 7)
					ft_putchar(',');
				if (nb[0] != 7)
					ft_putchar(' ');
				nb[2] += 1;
			}
			nb[1] += 1;
		}
		nb[0] += 1;
	}
}
