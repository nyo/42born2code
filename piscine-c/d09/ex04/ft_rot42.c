char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (str[i] + 16 <= 'z')
				str[i] += 16;
			else if (str[i] + 16 > 'z')
				str[i] -= 10;
		}
		else if (str[i] >= 'A' && str[i] <= 'Z')
		{
			if (str[i] + 16 <= 'Z')
				str[i] += 16;
			else if (str[i] + 16 > 'Z')
				str[i] -= 10;
		}
		i += 1;
	}
	return (str);
}
