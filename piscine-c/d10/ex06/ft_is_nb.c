#include "protodo.h"

int		ft_is_nb(char *str)
{
	int i;
	int nb;
	int neg;

	i = 0;
	nb = 0;
	neg = 0;
	while (str[i] < 33)
		i += 1;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '+' || str[i] == '-')
		i += 1;
	if (str[i] >= '0' && str[i] <= '9')
		return (1);
	return (0);
}
