int		ft_iterative_power(int nb, int power)
{
	int i;
	int res;

	i = 0;
	res = 1;
	if (power >= 0)
	{
		while (i < power)
		{
			res = res * nb;
			i += 1;
		}
		return (res);
	}
	else
	{
		return (0);
	}
}
