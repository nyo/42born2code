int		ft_sqrt(int nb)
{
	long i;

	i = 0;
	while (i * i <= nb)
	{
		i += 1;
	}
	i -= 1;
	if (i * i == nb)
	{
		return (i);
	}
	else
	{
		return (0);
	}
}
