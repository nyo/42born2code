int		ft_is_prime(int nb)
{
	int i;

	i = 5;
	if (nb == 2147483647 || nb == 2)
		return (1);
	else if ((nb <= 1) || (nb % 2 == 0) || (nb % 3 == 0))
		return (0);
	else if (nb <= 3)
		return (1);
	while (i * i <= nb)
	{
		if ((nb % i == 0) || (nb % (i + 2) == 0))
			return (0);
		i += 6;
	}
	return (1);
}
