#include "protocolle.h"

void	ft_wich_colle(char *l1, char *l2)
{
	int i;

	i = 0;
	if (l1[0] == 'o')
	{
		ft_putstr("colle-00");
	}
	if (l1[0] == '/')
	{
		ft_putstr("colle-01");
	}
	if (l1[0] == 'A' && l1[1] == 'B' && l1[2] == 'A')
	{
		ft_putstr("colle-02");
	}
	if (l1[0] == 'A' && l1[1] == 'B' && l1[2] == 'C'
		&& l2[0] == 'A' && l2[1] == 'B' && l2[2] == 'C')
	{
		ft_putstr("colle-03");
	}
	if (l1[0] == 'A' && l1[1] == 'B' && l1[2] == 'C'
		&& l2[0] == 'C' && l2[1] == 'B' && l2[2] == 'A')
	{
		ft_putstr("colle-04");
	}
}
