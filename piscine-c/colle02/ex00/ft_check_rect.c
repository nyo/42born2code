#include "protocolle.h"

int		ft_check_rect(char *str)
{
	int i;
	int j;
	int k;

	i = 0;
	k = 0;
	while (i < ft_height(str))
	{
		j = 0;
		while (str[k] != '\n')
		{
			j += 1;
			k += 1;
		}
		k += 1;
		if (ft_width(str) != j)
			return (0);
		i += 1;
	}
	return (1);
}
