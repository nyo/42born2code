int		ft_check_argv(char *str)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (!((str[i] >= '0' && str[i] <= '9') || str[i] == '.'))
		{
			j += 1;
		}
		i += 1;
	}
	if (i == 9 && j == 0)
		return (1);
	else
		return (0);
}
