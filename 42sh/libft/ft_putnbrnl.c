#include "libft.h"

/*
**	-------------------------------------------------------------------------- +
**	Outputs the integer `n` to the standard output followed by a ’\n’.
**	-------------------------------------------------------------------------- +
*/

void	ft_putnbrnl(int n)
{
	ft_putnbr(n);
	ft_putchar('\n');
}
