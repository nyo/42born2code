/*
**	-------------------------------------------------------------------------- +
**	man 3 strlen
**	-------------------------------------------------------------------------- +
*/

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = -1;
	while (*(s + ++i))
		;
	return (i);
}
