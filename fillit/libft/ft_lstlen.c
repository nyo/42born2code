#include "libft.h"

size_t	ft_lstlen(t_list *lst)
{
	t_list	*tmp;
	size_t	len;

	len = 0;
	tmp = lst;
	while (tmp)
	{
		tmp = tmp->next;
		len++;
	}
	return (len);
}
