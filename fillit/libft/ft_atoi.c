#include "libft.h"

int		ft_atoi(const char *str)
{
	int ret;
	int	neg;

	ret = 0;
	neg = 0;
	while ((*str == ' ') || (*str == '\f') || (*str == '\n')
			|| (*str == '\r') || (*str == '\t') || (*str == '\v'))
		str++;
	if (*str == '-')
		neg = 1;
	if (*str == '+' || *str == '-')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		ret *= 10;
		ret += (int)(*str - '0');
		str++;
	}
	return (neg == 1 ? -ret : ret);
}
