#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*nextlst;

	if (lst)
	{
		while (lst)
		{
			nextlst = lst->next;
			f(lst);
			lst = nextlst;
		}
	}
}
