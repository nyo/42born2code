#include "libft.h"

/*
**	-------------------------------------------------------------------------- +
**	Outputs the character c to the standard output followed by a ’\n’.
**	-------------------------------------------------------------------------- +
*/

void	ft_putcharnl(char c)
{
	ft_putchar(c);
	ft_putchar('\n');
}
