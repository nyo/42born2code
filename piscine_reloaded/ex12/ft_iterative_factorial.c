int		ft_iterative_factorial(int nb)
{
	int i;
	int res;

	if (nb >= 0 && nb <= 12)
	{
		i = 1;
		res = 1;
		while (i <= nb)
		{
			res = i * res;
			i++;
		}
		return (res);
	}
	else
		return (0);
}
