#include "libft.h"

/*
**	ft_putarr() prints the content of a `char **`
*/

void	ft_putarr(char **arr)
{
	int	i;

	i = 0;
	while (*(arr + i))
	{
		ft_putendl(*(arr + i));
		i++;
	}
}
